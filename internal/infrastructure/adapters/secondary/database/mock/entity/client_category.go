// UpdateClientCategory updates an existing client_category in stateful storage
func (r *MockClientCategoryRepository) UpdateClientCategory(ctx context.Context, req *clientcategorypb.UpdateClientCategoryRequest) (*clientcategorypb.UpdateClientCategoryResponse, error) {
	if req == nil {
		return nil, fmt.Errorf("update client_category request is required")
	}
	if req.Data == nil {
		return nil, fmt.Errorf("client_category data is required for update")
	}

	r.mutex.Lock()
	defer r.mutex.Unlock()

	var existingClientCategory *clientcategorypb.ClientCategory
	var exists bool
	var simpleKey string
	var compositeKey string

	// Try simple ID lookup first (for test compatibility)
	if req.Data.Id != "" {
		existingClientCategory, exists = r.clientCategories[req.Data.Id]
		simpleKey = req.Data.Id
	}

	// Fallback to composite key lookup
	if !exists && req.Data.ClientId != "" && req.Data.CategoryId != "" {
		compositeKey = fmt.Sprintf("%s-%s", req.Data.ClientId, req.Data.CategoryId)
		existingClientCategory, exists = r.clientCategoriesByKey[compositeKey]
	}

	if !exists {
		return nil, fmt.Errorf("client_category with client ID '%s' and category ID '%s' does not exist", req.Data.ClientId, req.Data.CategoryId)
	}

	// Update preserving use case timestamps and nested objects
	updatedClientCategory := &clientcategorypb.ClientCategory{
		Id:          req.Data.Id,
		ClientId:    req.Data.ClientId,
		CategoryId:  req.Data.CategoryId,
		Active:      req.Data.Active,
		// Preserve creation timestamps
		DateCreated:       existingClientCategory.DateCreated,
		DateCreatedString: existingClientCategory.DateCreatedString,
		// Use timestamps from use case (correct millisecond units)
		DateModified:       req.Data.DateModified,
		DateModifiedString: req.Data.DateModifiedString,
	}

	// Copy nested Category object if provided
	if req.Data.Category != nil {
		updatedClientCategory.Category = req.Data.Category
	} else {
		updatedClientCategory.Category = existingClientCategory.Category
	}

	// Update in both maps for dual access patterns
	if simpleKey != "" {
		r.clientCategories[simpleKey] = updatedClientCategory
	}
	if compositeKey == "" {
		compositeKey = fmt.Sprintf("%s-%s", updatedClientCategory.ClientId, updatedClientCategory.CategoryId)
	}
	r.clientCategoriesByKey[compositeKey] = updatedClientCategory

	return &clientcategorypb.UpdateClientCategoryResponse{
		Data:    []*clientcategorypb.ClientCategory{updatedClientCategory},
		Success: true,
	}, nil
}

// DeleteClientCategory deletes a client_category from stateful storage
func (r *MockClientCategoryRepository) DeleteClientCategory(ctx context.Context, req *clientcategorypb.DeleteClientCategoryRequest) (*clientcategorypb.DeleteClientCategoryResponse, error) {
	if req == nil {
		return nil, fmt.Errorf("delete client_category request is required")
	}
	if req.Data == nil {
		return nil, fmt.Errorf("client_category data is required for deletion")
	}

	r.mutex.Lock()
	defer r.mutex.Unlock()

	var existingClientCategory *clientcategorypb.ClientCategory
	var exists bool
	var simpleKey string
	var compositeKey string

	// Try simple ID lookup first (for test compatibility)
	if req.Data.Id != "" {
		existingClientCategory, exists = r.clientCategories[req.Data.Id]
		simpleKey = req.Data.Id
		if exists {
			compositeKey = fmt.Sprintf("%s-%s", existingClientCategory.ClientId, existingClientCategory.CategoryId)
		}
	}

	// Fallback to composite key lookup
	if !exists && req.Data.ClientId != "" && req.Data.CategoryId != "" {
		compositeKey = fmt.Sprintf("%s-%s", req.Data.ClientId, req.Data.CategoryId)
		existingClientCategory, exists = r.clientCategoriesByKey[compositeKey]
		if exists {
			simpleKey = existingClientCategory.Id
		}
	}

	if !exists {
		return nil, fmt.Errorf("client_category with client ID '%s' and category ID '%s' does not exist", req.Data.ClientId, req.Data.CategoryId)
	}

	// Perform actual deletion from both persistent stores
	if simpleKey != "" {
		delete(r.clientCategories, simpleKey)
	}
	if compositeKey != "" {
		delete(r.clientCategoriesByKey, compositeKey)
	}

	return &clientcategorypb.DeleteClientCategoryResponse{
		Success: true,
	}, nil
}

// ListClientCategories retrieves all client_categories from stateful storage
func (r *MockClientCategoryRepository) ListClientCategories(ctx context.Context, req *clientcategorypb.ListClientCategoriesRequest) (*clientcategorypb.ListClientCategoriesResponse, error) {
	r.mutex.RLock()
	defer r.mutex.RUnlock()

	// Use composite key map as the authoritative source (avoids duplicates)
	clientCategories := make([]*clientcategorypb.ClientCategory, 0, len(r.clientCategoriesByKey))
	for _, clientCategory := range r.clientCategoriesByKey {
		clientCategories = append(clientCategories, clientCategory)
	}

	// Use the list data processor to handle filtering, sorting, searching, and pagination
	result, err := r.processor.ProcessListRequest(
		clientCategories,
		req.Pagination,
		req.Filters,
		req.Sort,
		req.Search,
	)
	if err != nil {
		return nil, fmt.Errorf("failed to process client_category list data: %w", err)
	}

	// Convert processed items back to client_category protobuf format
	processedClientCategories := make([]*clientcategorypb.ClientCategory, len(result.Items))
	for i, item := range result.Items {
		if clientCategory, ok := item.(*clientcategorypb.ClientCategory); ok {
			processedClientCategories[i] = clientCategory
		} else {
			return nil, fmt.Errorf("failed to convert item to client_category type")
		}
	}

	return &clientcategorypb.ListClientCategoriesResponse{
		Data:    processedClientCategories,
		Success: true,
	}, nil
}

