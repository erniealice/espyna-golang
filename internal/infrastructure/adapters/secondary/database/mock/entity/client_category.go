// loadInitialData loads mock data from the copya package
func (r *MockClientCategoryRepository) loadInitialData() error {
	r.mutex.Lock()
	defer r.mutex.Unlock()

	if r.initialized {
		return nil // Prevent double initialization
	}

	// Load from @leapfor/copya package
	rawClientCategories, err := datamock.LoadClientCategories(r.businessType)
	if err != nil {
		return fmt.Errorf("failed to load initial client categories: %w", err)
	}

	// Convert and store each client category in both maps
	for _, rawClientCategory := range rawClientCategories {
		if clientCategory, err := r.mapToProtobufClientCategory(rawClientCategory); err == nil {
			// Store by simple ID (for test compatibility)
			if clientCategory.Id != "" {
				r.clientCategories[clientCategory.Id] = clientCategory
			}
			// Store by composite key (for business logic)
			compositeKey := fmt.Sprintf("%s-%s", clientCategory.ClientId, clientCategory.CategoryId)
			r.clientCategoriesByKey[compositeKey] = clientCategory
		}
	}

	r.initialized = true
	return nil
}

// CreateClientCategory creates a new client_category with stateful storage
func (r *MockClientCategoryRepository) CreateClientCategory(ctx context.Context, req *clientcategorypb.CreateClientCategoryRequest) (*clientcategorypb.CreateClientCategoryResponse, error) {
	// Input validation
	if req == nil {
		return nil, fmt.Errorf("create client_category request is required")
	}
	if req.Data == nil {
		return nil, fmt.Errorf("client_category data is required")
	}
	if req.Data.ClientId == "" {
		return nil, fmt.Errorf("client_category clientId is required")
	}
	if req.Data.CategoryId == "" {
		return nil, fmt.Errorf("client_category categoryId is required")
	}

	r.mutex.Lock()
	defer r.mutex.Unlock()

	// Generate unique key with client and category IDs
	key := fmt.Sprintf("%s-%s", req.Data.ClientId, req.Data.CategoryId)

	// Check if already exists
	if _, exists := r.clientCategoriesByKey[key]; exists {
		return nil, fmt.Errorf("client_category with clientId '%s' and categoryId '%s' already exists", req.Data.ClientId, req.Data.CategoryId)
	}

	// Create new client_category preserving use case data
	newClientCategory := &clientcategorypb.ClientCategory{
		Id:          req.Data.Id, // Preserve ID generated by use case
		ClientId:    req.Data.ClientId,
		CategoryId:  req.Data.CategoryId,
		Active:      req.Data.Active, // Preserve active flag set by use case
		// Use timestamps from use case (correct millisecond units)
		DateCreated:        req.Data.DateCreated,
		DateCreatedString:  req.Data.DateCreatedString,
		DateModified:       req.Data.DateModified,
		DateModifiedString: req.Data.DateModifiedString,
	}

	// Copy nested Category object if provided (key fix for nested object preservation)
	if req.Data.Category != nil {
		newClientCategory.Category = req.Data.Category
	}

	// Store in both maps for dual access patterns
	if newClientCategory.Id != "" {
		r.clientCategories[newClientCategory.Id] = newClientCategory
	}
	r.clientCategoriesByKey[key] = newClientCategory

	return &clientcategorypb.CreateClientCategoryResponse{
		Data:    []*clientcategorypb.ClientCategory{newClientCategory},
		Success: true,
	}, nil
}

