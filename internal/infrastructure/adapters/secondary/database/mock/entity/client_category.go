// mapToProtobufClientCategory converts raw mock data to protobuf ClientCategory
func (r *MockClientCategoryRepository) mapToProtobufClientCategory(rawClientCategory map[string]any) (*clientcategorypb.ClientCategory, error) {
	clientCategory := &clientcategorypb.ClientCategory{}

	// Map ID field (for test compatibility)
	if id, ok := rawClientCategory["id"].(string); ok {
		clientCategory.Id = id
	}

	// Map required fields
	if clientId, ok := rawClientCategory["clientId"].(string); ok {
		clientCategory.ClientId = clientId
	} else {
		return nil, fmt.Errorf("missing or invalid clientId field")
	}

	if categoryId, ok := rawClientCategory["categoryId"].(string); ok {
		clientCategory.CategoryId = categoryId
	} else {
		return nil, fmt.Errorf("missing or invalid categoryId field")
	}

	// Map active status
	if active, ok := rawClientCategory["active"].(bool); ok {
		clientCategory.Active = active
	} else {
		clientCategory.Active = true // Default to active
	}

	// Handle date fields - convert string timestamps to Unix timestamps
	if dateCreated, ok := rawClientCategory["dateCreated"].(string); ok {
		clientCategory.DateCreatedString = &dateCreated
		if timestamp, err := r.parseTimestamp(dateCreated); err == nil {
			clientCategory.DateCreated = &timestamp
		}
	}

	if dateModified, ok := rawClientCategory["dateModified"].(string); ok {
		clientCategory.DateModifiedString = &dateModified
		if timestamp, err := r.parseTimestamp(dateModified); err == nil {
			clientCategory.DateModified = &timestamp
		}
	}

	return clientCategory, nil
}

// parseTimestamp converts string timestamp to Unix timestamp
func (r *MockClientCategoryRepository) parseTimestamp(timestampStr string) (int64, error) {
	// Direct timestamp parsing (already in milliseconds)
	if timestamp, err := strconv.ParseInt(timestampStr, 10, 64); err == nil {
		return timestamp, nil
	}

	// RFC3339 parsing converted to milliseconds
	if t, err := time.Parse(time.RFC3339, timestampStr); err == nil {
		return t.UnixMilli(), nil // Consistent milliseconds
	}

	// Try parsing as other common formats
	formats := []string{
		"2006-01-02T15:04:05Z",
		"2006-01-02 15:04:05",
		"2006-01-02T15:04:05.000Z",
	}

	for _, format := range formats {
		if t, err := time.Parse(format, timestampStr); err == nil {
			return t.UnixMilli(), nil // Consistent milliseconds
		}
	}

	return 0, fmt.Errorf("unable to parse timestamp: %s", timestampStr)
}

